package data_types;

public class DataTypes {

	public static void main(String[] args) {
		byte a = 1; //signed number
		short a1 = 1; //short 16 Bit integer
		int a2=1; //32 Bit integer
		long a3=1; //64 Bit Integer
		//byte, short, int, long are all signed
		
		float b = 1.0f;//32 Bit
		double b1 = 1.0f;//64 bit
		
		boolean c=true;//Standard bool
		
		String d ="Hallo";//Not a character array. 
		
		int[] e = new int[3]; //Integer array. Typesafe pointer with length method. Not a class though
		
	}

}
